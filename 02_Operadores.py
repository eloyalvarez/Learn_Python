### Operadores Aritmeticos ###
 
print( 3 + 4 )
print( 3 - 4 )
print( 3 * 4 )
print( 3 / 4 )
print( 10 % 3 )
print( 10 // 3 )
print( 2 ** 3 )

print("Hola " + "Python")
print("Hola " + str(5))
print("Hola " * 5)
print("Hola " * (2**3))

my_float = 2.5 * 2
print("Hola " * int(my_float))


### Operadores Comparativos ###

print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)
print(3 == 4)
print(3 != 4)

print("Hola" < "Python" )
print("aaaa" >= "abaa") # Ordenación alfabética ASCII
print(len("aaaa") >= len("abaa")) # Ordenación cantidad de caracteres


### Operadores lógicos ###
print(3 > 4 and "Hola" > "Python")
print(3 > 4 or "Hola" > "Python")
print( not (3 > 4))